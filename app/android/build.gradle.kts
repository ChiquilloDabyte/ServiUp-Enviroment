import com.android.build.gradle.LibraryExtension

allprojects {
    repositories {
        google()
        mavenCentral()
    }
}

val newBuildDir: Directory =
    rootProject.layout.buildDirectory
        .dir("../../build")
        .get()
rootProject.layout.buildDirectory.value(newBuildDir)

subprojects {
    val newSubprojectBuildDir: Directory = newBuildDir.dir(project.name)
    project.layout.buildDirectory.value(newSubprojectBuildDir)
}
subprojects {
    afterEvaluate {
        extensions.findByType(LibraryExtension::class.java)?.let { androidExtension ->
            if (androidExtension.namespace.isNullOrBlank()) {
                val fallbackNamespace = project.group?.toString()?.takeIf { it.isNotBlank() }
                    ?: "dev.isar.${project.name}"
                androidExtension.namespace = fallbackNamespace
            }
        }
    }
}

subprojects {
    project.evaluationDependsOn(":app")
}

tasks.register<Delete>("clean") {
    delete(rootProject.layout.buildDirectory)
}
